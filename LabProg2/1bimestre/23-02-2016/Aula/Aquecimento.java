    public static int somatorio(int[] valores) {
    
        
        int total = 0;
        
        // realiza um laço de zero até o tamanho do array valores, e realiza a soma dos valores.
        for (int i  = 0; i < valores.length; i++){
            total += valores[i];
        }
        return total;
    
    }
    
